// Populating dropdown options and setting default values
for (let select of dropdowns) {
  for (let currCode in countryList) {
    let newOption = document.createElement("option");
    newOption.innerText = currCode;
    newOption.value = currCode;
    // Setting default selection for 'from' currency dropdown as USD
    if (select.name === "from" && currCode === "USD") {
      newOption.selected = "selected";
    }
    // Setting default selection for 'to' currency dropdown as INR
    else if (select.name === "to" && currCode === "INR") {
      newOption.selected = "selected";
    }
    select.append(newOption);
  }

  // Adding event listener to update flag when currency is changed
  select.addEventListener("change", (evt) => {
    updateFlag(evt.target);
  });
}

// Function to update exchange rate and display message
const updateExchangeRate = async () => {
  let amount = document.querySelector(".amount input");
  let amtVal = amount.value;
  console.log(amtVal);
  if (amtVal === "" || amtVal < 1) {
    amtVal = 1;
    amount.value = "1";
  }
  // Constructing API URL for fetching exchange rate
  const URL = `${BASE_URL}/${fromCurr.value.toLowerCase()}.json`;
  let response = await fetch(URL);
  let data = await response.json();
  let rate = data[toCurr.value.toLowerCase()];

  // Calculating final amount after conversion
  let finalAmount = (amtVal * rate).toFixed(2);
  // Displaying conversion message
  msg.innerText = `${amtVal} ${fromCurr.value} = ${finalAmount} ${toCurr.value}`;
};

// Function to update flag based on selected currency
const updateFlag = (element) => {
  let currCode = element.value;
  let countryCode = countryList[currCode];
  let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png`;
  let img = element.parentElement.querySelector("img");
  if (!img) {
    img = document.createElement("img");
    element.parentElement.appendChild(img);
  }
  img.src = newSrc;
};

// Adding click event listener to form buttons
btn.addEventListener("click", (evt) => {
  evt.preventDefault();
  // Calling function to update exchange rate
  updateExchangeRate();
});

// Adding load event listener to update exchange rate on page load
window.addEventListener("load", () => {
  // Calling function to update exchange rate
  updateExchangeRate();
});
